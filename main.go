func quickDecode(arr []byte) {
	packetID, buffer := network.Decrypt(arr)
	fmt.Printf("ID=0x%04X, Size=%d, Total=%d\n", packetID, buffer.Len(), buffer.Len()+7)
	fmt.Println(hex.Dump(buffer.Bytes()))
}

